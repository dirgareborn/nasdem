                    <div class="section-title-left">
                    <h4 class="text-bold">2013 – Kongres I Partai NasDem dan kelolosan verifikasi faktual </h4>
                    </div>
                    <div class="entry-content">
                    <p style="text-align:justify">
                    Kongres I Partai NasDem yang digelar pada 25-26 Januari 2013 di Jakarta menjadi tonggak sejarah perjalanan Partai NasDem. Berbagai keputusan penting dikeluarkan dalam kongres ini. Satu di antaranya ialah memilih dan menetapkan Surya Paloh sebagai Ketua Umum Dewan Pimpinan Pusat (DPP) Partai NasDem periode 2013-2018. Ibarat perahu, layar telah terkembang, lengkap dengan nahkoda dan awak kapal.
                    </p>
                    <p style="text-align:justify">
                    Kongres I Partai NasDem saat itu diikuti 66 orang yang mewakili 33 DPW, 994 orang mewakili 497 DPD, 9 orang mewakili Majelis Tinggi, dan 2 orang anggota Dewan Pakar. Selain peserta yang memiliki hak suara, Kongres juga dihadiri 800 orang peninjau yang datang dari seluruh penjuru Indonesia.
                    </p>
                    <p style="text-align:justify">
                    Keputusan diambil pada sidang pleno pertama tanggal 25 Januari 2013 sekitar pukul 23.00 WIB. Seluruh 33 Dewan Pimpinan Wilayah (DPW), 497 Dewan Pimpinan Daerah (DPD), dan empat organisasi sayap (Gerakan Massa Buruh, Liga Mahasiswa, Badan Advokasi Hukum, dan Petani NasDem), bersatu suara memercayakan Surya Paloh menjadi nakhoda Partai NasDem selama lima tahun.
                    </p>
                    <p style="text-align:justify">
                    KPU mengumumkan hasil verifikasi faktual dan menyatakan Partai NasDem lolos dalam memenuhi persyaratan verifikasi faktual tingkat pusat sebagaimana diatur dalam UU Pemilu Nomor 15 Tahun 2011 tentang Penyelenggaraan Pemilu. Partai NasDem memenuhi semua syarat verifikasi faktual di seluruh provinsi, dengan bukti-bukti, antara lain:
                    </p>
                    <ul>
                        <li>Memiliki kepengurusan seperti Ketua Umum, Sekretaris Jenderal, dan Bendahara Umum.</li>
                        <li>Memiliki lebih dari 30% fungsionaris perempuan.</li>
                        <li>Memiliki kantor yang digunakan sampai akhir Pemilu 2014.</li>
                    </ul>
                    <p style="text-align:justify">
                    Partai NasDem adalah satu-satunya partai baru yang lolos sebagai peserta Pemilu 2014. Sehingga, Partai NasDem berhak mengikuti pemilu untuk pertama kalinya pada 2014.
                    </div>

