                    </p>
                    <p style="text-align:justify">
                    Pada Pemilu 2014, Partai NasDem juga secara terbuka menjadi deklarator mendukung Joko Widodo – Jusuf Kalla sebagai Capres – Cawapres RI 2014-2019.
                    </p>
                    <p style="text-align:justify">
                    Partai NasDem menggelar deklarasi mendukung Joko Widodo sebagai Calon Presiden (Capres), bersama dengan Partai Demokrasi Indonesia Perjuangan (PDIP) dan Partai Kebangkitan Bangsa (PKB) di kantor DPP PDI Perjuangan, Lenteng Agung, Jakarta Selatan pada Rabu 14 Mei 2014
                    </p>
                    <p style="text-align:justify">
                    Ketua Umum Partai NasDem Surya Paloh menegaskan bahwa keputusan partainya mendukung Joko Widodo (Jokowi) didasari keyakinan bahwa Jokowi bisa memimpin negara untuk mengatasi berbagai permasalahan kompleks.
                    </p>
                    <p style="text-align:justify">
                    NasDem menyatakan dukungannya tanpa syarat apapun. Dukungan kepada Jokowi memang diniatkan dan diikhlaskan secara sungguh-sungguh demi memperbaiki nasib bangsa dan rakyat.
                    </p>
                    <p style="text-align:justify">
                    Partai NasDem ingin memperjuangkan kembali keberadaan idealisme di tengah apatisme publik terhadap politik. Partai NasDem ingin memperjuangkan idealisme kehidupan kebangsaan yang jauh lebih penting dari kepentingan pribadi, kelompok, atau partai.
                    </p>
                    <p style="text-align:justify">
                    Berdasar lampiran SK KPU No. 411/Kpts/KPU/2014 tentang penetapan hasil Pemilu 2014, Partai NasDem akhirnya mendapat terbanyak kursi kesembilan dengan 8.402.812 suara. Sebanyak 35 kursi parlemen diraih NasDem, dengan 6,72 persen atau 8.402.812 suara.
